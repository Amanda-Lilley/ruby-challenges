puts "What is the weather today?"
weather = gets.chomp

case weather
when 'rainy'
  puts "Get your rubber boots on and lets dance in the rain!"
when 'cold'
  puts "Finally! It is time to break out those awesome hand knits!"
when 'sunny'
  puts "Grab your bikini!"
when 'windy'
  puts "Hold your hat!"
end
